const resultado = nota => nota >= 7 ? 'Aprovado' : 'Reprovado'

/* 

    1ª Parte do Operador:
        (nota >= 7) - expressão
    2ª Parte do Operador:
        verdadeiro = 'Aprovado' 
    3ª Parte do Operador:
        falso = 'Reprovado'

*/

console.log(resultado(7.1)) // teste = Aprovado
console.log(resultado(4.3)) // teste = Reprovado
